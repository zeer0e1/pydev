# Python funcção if ...elif
numero3 = 6
numero4 = 6

# Elif é usado para verificar mais uma condição
if numero3 > numero4:
    print('O valor da variavel 1 é maior que a variavel 2')
elif numero3 == numero4:
    print('As variaveis são iguais')
# exemplo pratico
numeros = [6, 3, 7, 15, 2, 7, 15]

# Está funcão retorna o número de elementos na lista
tamanho = len(numeros)
print(f'O número de elementos na lista é: {tamanho}')

# Max() retorna o maior valor
maior_valor = max(numeros)
print(f'O maior valor na lista é: {maior_valor}')

# Min() Retorna menor valor
menor_valor = min(numeros)
print(f'O menor valor na lista é: {menor_valor}')

# sum() retorna a soma dos elementos
soma_dos_elemetnos = sum(numeros)
print(f'A soma dos elementos da lista é: {soma_dos_elemetnos}')
